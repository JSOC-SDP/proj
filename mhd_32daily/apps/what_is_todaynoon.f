       integer function mmdd2doy(yy, mm, idd)
       implicit none
       integer yy, mm, idd
       integer dyofmth
       integer mm2, idd2, idoy

       if (mm .EQ. 1) then
         idd2 = idd
       else if (mm .GT. 1) then
         idd2 = 0
         do mm2 = 1, mm - 1
           idd2 = idd2 + dyofmth(yy,mm2)
         enddo
         idd2 = idd2 + idd
       else
         write(*,*) ' MM is wrong '
       endif
       idoy = idd2
       mmdd2doy = idoy
       return
       end


