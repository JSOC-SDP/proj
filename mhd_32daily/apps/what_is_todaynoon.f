       subroutine shiftday(yy1,mm1,idd1,idshift)
       implicit none
* interface
       integer yy1, mm1, idd1, idshift
* local
       integer dyofmth
       integer idummy

       if (abs(idshift) .GT. 28) write(*,*) ' Dshift is too big !!'

       if (idshift .GE. 0) then
         idd1 = idd1 + idshift
         idummy = dyofmth(yy1, mm1)
         if (idd1 .GT. idummy) then
           idd1 = idd1 - idummy
           mm1  = mm1 + 1
         endif
         if (mm1 .GT. 12) then
           mm1 = mm1 - 12
           yy1 = yy1 + 1
         endif
       else
         idd1 = idd1 + idshift
         if (idd1 .LE. 0) then
           mm1 = mm1 - 1
           if (mm1 .GE. 1) then
             idd1 = idd1 + dyofmth(yy1,mm1)
           else
             mm1 = 12
             yy1 = yy1 - 1
             idd1 = idd1 + dyofmth(yy1,mm1)
           endif
         endif
       endif

       return
       end


